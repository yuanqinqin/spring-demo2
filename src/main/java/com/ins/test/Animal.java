package com.ins.test;

public class Animal {
    private int id;
    private String name;
    private String color;
    private int age;

    public void setId(int id){
        this.id = id;
    }
    public int getId(){
        return this.id;
    }

    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }

    public void setColor(String color){
        this.color = color;
    }
    public String getColor(){
        return this.color;
    }


    public void setAge(int age){
        this.age = age;
    }
    public int getAge(){
        return this.age;
    }

}
